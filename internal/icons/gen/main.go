//go:build ignore

// Codegen for internal/icons/icons_data.go.
//
// Reads glyphnames.json (vendored alongside this file) and writes a
// Go source file with the full nerd-font catalog as map[string]rune
// plus a sorted []string of names for iteration / search.
//
// Run with `go generate ./internal/icons/...`.
package main

import (
	"encoding/json"
	"fmt"
	"os"
	"path/filepath"
	"sort"
	"strconv"
	"strings"
)

type entry struct {
	Char string `json:"char"`
	Code string `json:"code"`
}

func main() {
	if err := run(); err != nil {
		fmt.Fprintln(os.Stderr, "icons gen:", err)
		os.Exit(1)
	}
}

func run() error {
	dir, err := genDir()
	if err != nil {
		return err
	}
	raw, err := os.ReadFile(filepath.Join(dir, "glyphnames.json"))
	if err != nil {
		return err
	}
	var doc map[string]json.RawMessage
	if err := json.Unmarshal(raw, &doc); err != nil {
		return fmt.Errorf("parse glyphnames.json: %w", err)
	}
	delete(doc, "METADATA")

	names := make([]string, 0, len(doc))
	runes := make(map[string]rune, len(doc))
	for name, ev := range doc {
		var e entry
		if err := json.Unmarshal(ev, &e); err != nil {
			return fmt.Errorf("entry %q: %w", name, err)
		}
		v, err := strconv.ParseUint(e.Code, 16, 32)
		if err != nil {
			return fmt.Errorf("entry %q: bad code %q: %w", name, e.Code, err)
		}
		names = append(names, name)
		runes[name] = rune(v)
	}
	sort.Strings(names)

	out := filepath.Join(filepath.Dir(dir), "icons_data.go")
	return writeData(out, names, runes)
}

func genDir() (string, error) {
	cwd, err := os.Getwd()
	if err != nil {
		return "", err
	}
	candidates := []string{
		filepath.Join(cwd, "internal", "icons", "gen"),
		filepath.Join(cwd, "gen"),
		cwd,
	}
	for _, c := range candidates {
		if _, err := os.Stat(filepath.Join(c, "glyphnames.json")); err == nil {
			return c, nil
		}
	}
	return "", fmt.Errorf("could not locate glyphnames.json (looked in %v)", candidates)
}

func writeData(path string, names []string, runes map[string]rune) error {
	var b strings.Builder
	b.WriteString("// Code generated by internal/icons/gen. DO NOT EDIT.\n\n")
	b.WriteString("package icons\n\n")

	fmt.Fprintf(&b, "// nfCount is the number of glyphs shipped. Regenerate via\n")
	fmt.Fprintf(&b, "// `go generate ./internal/icons/...`.\n")
	fmt.Fprintf(&b, "const nfCount = %d\n\n", len(names))

	b.WriteString("// nfNames is every glyph name, sorted ascending. Used for\n")
	b.WriteString("// stable iteration and search; the rune lives in nfGlyphs.\n")
	fmt.Fprintf(&b, "var nfNames = [...]string{\n")
	for _, n := range names {
		fmt.Fprintf(&b, "\t%q,\n", n)
	}
	b.WriteString("}\n\n")

	b.WriteString("// nfGlyphs maps a nerd-font name to its rune.\n")
	fmt.Fprintf(&b, "var nfGlyphs = map[string]rune{\n")
	for _, n := range names {
		r := runes[n]
		fmt.Fprintf(&b, "\t%q: 0x%X, // %s\n", n, r, string(r))
	}
	b.WriteString("}\n")

	return os.WriteFile(path, []byte(b.String()), 0o644)
}
